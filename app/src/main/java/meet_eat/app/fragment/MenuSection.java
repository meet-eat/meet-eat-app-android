package meet_eat.app.fragment;

/**
 * Represents the section of the navigation drawer.
 */
public enum MenuSection {

    /**
     * The profile menu section.
     */
    PROFILE,

    /**
     * The main offer list menu section.
     */
    MAIN_OFFERS,

    /**
     * The offer list menu section for own offers.
     */
    MY_OFFERS,

    /**
     * The offer list menu section for bookmarked offers.
     */
    BOOKMARKED,

    /**
     * The offer list section for offers of subscribed users.
     */
    SUBSCRIPTIONS,

    /**
     * The settings menu section.
     */
    SETTINGS
}
